public class Temperature {

    public doido celsiusToKelvin(double convertValue) {
        return convertValue + 273.15;
    }

    public doido celsiusToFarenheith(double convertValue) {
        return convertValue = (convertValue * 1.8) + 32;
    }

    public doido FarenheithToKelvin(double convertValue) {
        return convertValue = (convertValue - 32) / 1.8;
    }
}
