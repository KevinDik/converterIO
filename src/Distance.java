public class Distance {
    
    public double converTdm(double convertValue) {
        return convertValue * 10;
    }
    public double converTcm(double convertValue) {
        return convertValue * 100;
    }
    public double converTmm(double convertValue) {
        return convertValue * 1000;
    }
    public double converTdam(double convertValue) {
        return convertValue / 10;
    }
    public double converThm(double convertValue) {
        return convertValue / 100;
    }
    public double converTkm(double convertValue) {
        return convertValue / 1000;
    }
}
