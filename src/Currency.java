public class Currency {
    public double convertRealtoDolar(double convertValue) {
        return convertValue * 0.19;
    }

    public double convertRealToEuro(double convertValue) {
        return convertValue * 0.18;
    }

    public double convertRealToLibra(double convertValue) {
        return convertValue * 0.16;
    }

    public double convertRealToPesoArgentino(double convertValue) {
        return convertValue * 38.02;
    }

    public double convertRealToCoroaSueca(double convertValue) {
        return convertValue * 0.16;
    }

    public double convertRealToCoroaNorueguesa(double convertValue) {
        return convertValue * 2.01;
    }


}
